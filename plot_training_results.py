import os
import pandas as pd
import matplotlib.pyplot as plt
import cv2
from pathlib import Path
import argparse

def plot_training_results(run_dir):
    """
    Reads the YOLO results.csv and explicitly plots the training metrics using matplotlib.
    """
    run_dir = Path(run_dir)
    csv_path = run_dir / "results.csv"
    
    if not csv_path.exists():
        print(f"Error: Could not find {csv_path}. Make sure training has started/finished.")
        return

    # YOLO results.csv often has leading spaces in column names
    df = pd.read_csv(csv_path)
    df.columns = df.columns.str.strip()

    epochs = df['epoch']
    
    # 1. Plot Losses (Box Loss and Class Loss)
    plt.figure(figsize=(14, 6))
    
    # Box Loss Subplot
    plt.subplot(1, 2, 1)
    if 'train/box_loss' in df.columns and 'val/box_loss' in df.columns:
        plt.plot(epochs, df['train/box_loss'], label='Train Box Loss', color='blue', linewidth=2)
        plt.plot(epochs, df['val/box_loss'], label='Val Box Loss', color='orange', linewidth=2, linestyle='--')
    plt.title('Bounding Box Loss per Epoch')
    plt.xlabel('Epoch')
    plt.ylabel('Loss')
    plt.legend()
    plt.grid(True, linestyle=':', alpha=0.7)

    # Class Loss Subplot
    plt.subplot(1, 2, 2)
    if 'train/cls_loss' in df.columns and 'val/cls_loss' in df.columns:
        plt.plot(epochs, df['train/cls_loss'], label='Train Class Loss', color='blue', linewidth=2)
        plt.plot(epochs, df['val/cls_loss'], label='Val Class Loss', color='orange', linewidth=2, linestyle='--')
    plt.title('Classification Loss per Epoch')
    plt.xlabel('Epoch')
    plt.ylabel('Loss')
    plt.legend()
    plt.grid(True, linestyle=':', alpha=0.7)
    
    plt.tight_layout()
    loss_plot_path = run_dir / "custom_loss_plot.png"
    plt.savefig(loss_plot_path, dpi=300)
    print(f"Saved custom loss plot to: {loss_plot_path}")
    plt.show()

    # 2. Plot mAP (Mean Average Precision)
    plt.figure(figsize=(8, 6))
    if 'metrics/mAP50(B)' in df.columns:
        plt.plot(epochs, df['metrics/mAP50(B)'], label='mAP @ 50', color='green', linewidth=2)
    if 'metrics/mAP50-95(B)' in df.columns:
        plt.plot(epochs, df['metrics/mAP50-95(B)'], label='mAP @ 50-95', color='purple', linewidth=2, linestyle='--')
    
    plt.title('Validation Accuracy (mAP) per Epoch')
    plt.xlabel('Epoch')
    plt.ylabel('mAP Score')
    plt.legend()
    plt.grid(True, linestyle=':', alpha=0.7)
    
    map_plot_path = run_dir / "custom_map_plot.png"
    plt.savefig(map_plot_path, dpi=300)
    print(f"Saved custom mAP plot to: {map_plot_path}")
    plt.show()

    # 3. Load and show the native Confusion Matrix if it exists
    cm_path = run_dir / "confusion_matrix.png"
    if cm_path.exists():
        plt.figure(figsize=(10, 10))
        img = cv2.imread(str(cm_path))
        img = cv2.cvtColor(img, cv2.COLOR_BGR2RGB)
        plt.imshow(img)
        plt.axis('off')
        plt.title("Confusion Matrix (Generated by YOLO)")
        plt.show()
    else:
        print("Confusion matrix image not found yet (usually generates at the end of training).")

if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="Plot YOLO training results using Matplotlib")
    # Default to the final fine-tuning stage of the helmet model
    parser.add_argument("--run_dir", type=str, default="outputs/training/helmet/stage3_finetune", 
                        help="Path to the training run directory containing results.csv")
    
    args = parser.parse_args()
    plot_training_results(args.run_dir)
